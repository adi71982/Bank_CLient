    public Savings_details(ArrayList<Long> accounts,long cust) {
	this.cust=cust;
	initComponents();
	try {
	    Socket s=new Socket(IPClass.host,7878);
	    ObjectOutputStream out=new ObjectOutputStream(s.getOutputStream());
	    ObjectInputStream in=new ObjectInputStream(s.getInputStream());
	    out.writeObject(accounts);
	    long account=in.readLong();
	    float amount=in.readFloat();
	    jLabel2.setText(String.valueOf(account));
	    jLabel4.setText(String.valueOf(amount));
	    in.close();
	    out.close();
	    s.close();
	} catch (IOException ex) {
	    Logger.getLogger(Savings_details.class.getName()).log(Level.SEVERE, null, ex);
	}
	
    }

